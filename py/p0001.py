def brute(n, k):
    return any(n[i] + n[j] == k for i in range(len(n) - 1) for j in range(i + 1, len(n)))


def sub(n, k):
    sub = []
    for i in n:
        sub.append(k - i)
        if i in sub:
            return True
    return False


